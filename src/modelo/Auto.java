package modelo;

/**
 *
 * @author Usuario
 */
public class Auto {
    int id;
    String Marca;
    String modelo;
    String patente;

    public Auto() {
    }

    public Auto(int id, String Marca, String modelo, String patente) {
        this.id = id;
        this.Marca = Marca;
        this.modelo = modelo;
        this.patente = patente;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getMarca() {
        return Marca;
    }

    public void setMarca(String Marca) {
        this.Marca = Marca;
    }

    public String getModelo() {
        return modelo;
    }

    public void setModelo(String modelo) {
        this.modelo = modelo;
    }

    public String getPatente() {
        return patente;
    }

    public void setPatente(String patente) {
        this.patente = patente;
    }
}
